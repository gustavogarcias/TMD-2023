    <h3>Parque Tanguá</h3>
    <img src='/assets/turismo/parque-tangua.png' alt='Parque Tanguá' class='image'>
    <p>O Tanguá, nome indígena que significa “baía das conchas”, é um dos parques preferidos de quem visita Curitiba. Um cartão postal da cidade cheio de atrativos naturais. Valem todos os momentos dedicados à sua contemplação.

Localizado em uma região de pedreiras desativadas e com topografia acidentada, característica da região norte da cidade, o parque recuperou e valorizou diferentes elementos da natureza num local antes degradado. Aquele espaço foi parte de pedreira da família Gava, desativada nos anos 70, e que depois passou duas décadas sem uso.</p>
