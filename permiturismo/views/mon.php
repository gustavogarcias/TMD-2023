    <h3>Museu Oscar Niemeyer</h3>
    <img src='/assets/turismo/mon.png' alt='Museu Oscar Niemeyer' class='image'>
    <p>O formato inusitado fez que com que o Museu Oscar Niemeyer ganhasse entre os moradores o apelido de Museu do Olho. Projetado pelo arquiteto e artista que dá nome ao museu, o MON é o melhor espaço dedicado à arte na cidade de Curitiba. O Museu do Olho dispõe de acervo permanente e também de mostras itinerantes. Entre os nomes de destaque no acervo do MON estão Andy Warhol, Tarsila do Amaral, Candido Portinari, Caribé, Tomie Ohtake e o próprio Oscar Niemeyer, que conta com uma mostra destinada a homenageá-lo.

Recentemente, o MON recebeu novo e interessante acervo. Peças confiscadas na Operação Lava Jato foram avaliadas pelo museu e hoje estão em exposição para o público. O prédio conta ainda com esculturas no jardim e no piso do museu, além de um gostoso café e uma lojinha cheia de boas surpresas.</p>
