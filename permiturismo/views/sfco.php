Na década dos anos 70 até meados de 2005, o prédio virou a moradia e o ateliê do escultor Ricardo Tod, autor da Fonte da Memória, conhecido como “Cavalo Babão” que fica ao lado do relógio das flores, no centro do Largo da Ordem.
O prédio então passou por projeto de regeneração do local, transformando-se em multi espaços super cool e revitalizando a rua charmosa.</p>
